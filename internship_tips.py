INTERNSHIP_TIPS = """
Here is a general, UW–Madison-aligned roadmap for finding an internship:

1) Start with reflection
- Clarify what you want to learn (skills, industries, locations).
- Think about timing (summer vs. semester, remote vs. in-person) and how it fits with your course load.

2) Use Handshake as your main search tool
- Log into Handshake with your NetID and complete your profile.
- Use filters for role type (internship), location, keywords, and your class year.
- Save searches and favorite positions or employers so you get alerts when new roles are posted.

3) Build a strong application (resume + cover letter)
- Tailor your resume to the role: highlight coursework, projects, student orgs, and part-time jobs that show relevant skills.
- Write a short, specific cover letter that connects your experiences to that particular internship.
- Avoid generic cover letters; show that you read the posting.

4) Network and talk to people
- Attend career & internship fairs, employer info sessions, and panels.
- Use LinkedIn or Badger Bridge to find alumni at organizations you’re interested in and ask for brief conversations.
- After talking to an employer, send a short thank-you email and connect on LinkedIn when appropriate.

5) Use SuccessWorks and campus resources
- Make an appointment with SuccessWorks for resume, cover letter, and internship search advising.
- Explore Career Communities to find resources tailored to your interests.
- Watch for SuccessWorks events focused on internships, interviewing, and employer networking.

6) Apply broadly and stay organized
- Apply to multiple internships; it’s normal to hear “no” before you hear “yes.”
- Track applications in a simple spreadsheet (company, role, deadline, status, contact).
- Follow up politely if you haven’t heard back after a reasonable amount of time.

7) Prepare for interviews
- Practice talking through your experiences using concrete examples (projects, jobs, leadership).
- Research each organization and prepare a few thoughtful questions to ask them.
- If you’re unsure how to prepare, schedule a mock interview with SuccessWorks or another campus career office.
"""
